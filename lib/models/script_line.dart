class ScriptLine {
  final String characterType;
  final double timing;
  final double speed;
  final String text;
  final double volume;
  final double pitch;
  final double intonation;
  

  ScriptLine({
    required this.characterType,
    required this.timing,
    required this.speed,
    required this.text,
    required this.volume,
    required this.pitch,
    required this.intonation,
  });
}